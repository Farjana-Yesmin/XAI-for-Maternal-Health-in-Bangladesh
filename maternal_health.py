class_names = le.classes_  # ['high risk', 'low risk', 'mid risk'] (alphabetical)
regions_list = df_test['Region'].unique()

equalized_odds_records = []

for region in sorted(regions_list):
    region_mask = df_test['Region'] == region
    y_true_r = df_test.loc[region_mask, 'Target'].values
    y_pred_r = df_test.loc[region_mask, 'Prediction'].values
    n_region = region_mask.sum()

    for class_idx, class_name in enumerate(class_names):
        # One-vs-rest: treat this class as positive
        y_true_bin = (y_true_r == class_idx).astype(int)
        y_pred_bin = (y_pred_r == class_idx).astype(int)

        TP = ((y_true_bin == 1) & (y_pred_bin == 1)).sum()
        FN = ((y_true_bin == 1) & (y_pred_bin == 0)).sum()
        FP = ((y_true_bin == 0) & (y_pred_bin == 1)).sum()
        TN = ((y_true_bin == 0) & (y_pred_bin == 0)).sum()

        TPR = TP / (TP + FN) if (TP + FN) > 0 else float('nan')
        FPR = FP / (FP + TN) if (FP + TN) > 0 else float('nan')

        equalized_odds_records.append({
            'Region': region,
            'Class': class_name,
            'n': n_region,
            'TPR': round(TPR, 3),
            'FPR': round(FPR, 3),
            'Access_Score': regions_df.loc[regions_df['Region'] == region, 'Healthcare_Access_Score'].values[0]
        })

equalized_odds_df = pd.DataFrame(equalized_odds_records)

